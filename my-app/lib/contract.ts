function validateEnvironment() {
  const requiredVars = {
    TICKET_NFT_CONTRACT_ADDRESS: process.env.TICKET_NFT_CONTRACT_ADDRESS,
    BNB_TESTNET_RPC_URL: process.env.BNB_TESTNET_RPC_URL,
  }

  const missing = Object.entries(requiredVars)
    .filter(([key, value]) => !value)
    .map(([key]) => key)

  if (missing.length > 0) {
    throw new Error(
      `Missing required environment variables: ${missing.join(", ")}. Please set them in your Project Settings.`,
    )
  }

  // Validate contract address format
  if (!ethers.isAddress(requiredVars.TICKET_NFT_CONTRACT_ADDRESS!)) {
    throw new Error(`Invalid contract address format: ${requiredVars.TICKET_NFT_CONTRACT_ADDRESS}`)
  }
}

// Contract configuration
export const CONTRACT_CONFIG = {
  get address() {
    validateEnvironment()
    return process.env.TICKET_NFT_CONTRACT_ADDRESS!
  },
  chainId: 97, // BNB Testnet
  get rpcUrl() {
    return process.env.BNB_TESTNET_RPC_URL || "https://data-seed-prebsc-1-s1.binance.org:8545/"
  },
}

// Create contract instance
export function getContract(signerOrProvider?: ethers.Signer | ethers.Provider) {
  try {
    const provider = signerOrProvider || new ethers.JsonRpcProvider(CONTRACT_CONFIG.rpcUrl)
    return new ethers.Contract(CONTRACT_CONFIG.address, TICKET_NFT_ABI, provider)
  } catch (error) {
    console.error("[v0] Contract initialization failed:", error)
    throw error
  }
}

// Create contract with signer for write operations
export function getContractWithSigner() {
  try {
    if (!process.env.PRIVATE_KEY) {
      throw new Error("Missing PRIVATE_KEY environment variable. Please set it in your Project Settings.")
    }

    const provider = new ethers.JsonRpcProvider(CONTRACT_CONFIG.rpcUrl)
    const wallet = new ethers.Wallet(process.env.PRIVATE_KEY, provider)
    return new ethers.Contract(CONTRACT_CONFIG.address, TICKET_NFT_ABI, wallet)
  } catch (error) {
    console.error("[v0] Contract with signer initialization failed:", error)
    throw error
  }
}

// Helper to format ticket data
export interface TicketData {
  tokenId: string
  owner: string
  used: boolean
  tokenURI?: string
}

export async function getTicketData(tokenId: string): Promise<TicketData | null> {
  try {
    console.log("[v0] Fetching ticket data for tokenId:", tokenId)
    const contract = getContract()
    console.log("[v0] Contract initialized successfully")

    const [owner, used, tokenURI] = await Promise.all([
      contract.ownerOf(tokenId),
      contract.used(tokenId),
      contract.tokenURI(tokenId).catch(() => ""),
    ])

    console.log("[v0] Ticket data fetched:", { tokenId, owner, used })
    return {
      tokenId,
      owner,
      used,
      tokenURI,
    }
  } catch (error) {
    console.error("[v0] Error fetching ticket data:", error)
    return null
  }
}
